    private void leerArchivo() {
    try {
        fis = new FileInputStream(fileChooser.getSelectedFile());
        isr = new InputStreamReader(fis, "ISO-8859-1");
        br =  new BufferedReader(isr);
        while((linea = br.readLine())!= null){ 
            txa_contenidoArchivo.append(linea);
            txa_contenidoArchivo.append("\n");
            numRenglon++;
            lexico = new Lexico(linea+(char)32,numRenglon);
            lexico.lexico();
            guardarTokens();
            guardarRenglon();
            txa_contenidoResultado.append(lexico.imprimir);
            txa_contenidoResultado.append(lexico.imprimirError);      
        }
  
    }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "No se pudo leer el Archivo: " + e);
        }
    }
    
    public void ejecutarCodigo(){
        String [] lineas = txa_contenidoArchivo.getText().split("\n");
        for(int i=0;i<lineas.length;i++){
            numRenglon++;
            lexico = new Lexico(lineas[i]+(char)32,numRenglon);
            lexico.lexico();
            guardarTokens();
            guardarRenglon();
            txa_contenidoResultado.append(lexico.imprimir);
            txa_contenidoResultado.append(lexico.imprimirError);      
        }
    }
    
    public void guardarTokens(){
        for (int i = 0; i < lexico.token.size(); i++) {
            this.token.add(lexico.token.get(i));
        }
    }

    private void guardarRenglon() {
        for (int i = 0; i < lexico.renglon.size(); i++) {
            this.renglon.add(lexico.renglon.get(i));
        }
    }
